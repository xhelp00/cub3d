#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*res;

	res = NULL;
	while (*s)
	{
		if (*s == c % 256)
			res = (char *) s;
		s++;
	}
	if (!c)
		res = (char *) s;
	return (res);
}
